<script>
    function mostrarNoticia(el) {
        const titulo = el.getAttribute('data-titulo');
        const data = el.getAttribute('data-data');
        const imagem = el.getAttribute('data-img');
        const conteudo = el.getAttribute('data-conteudo');

        document.getElementById('modalTitulo').innerText = titulo;
        document.getElementById('modalData').innerText = data;
        document.getElementById('modalImagem').src = imagem;
        document.getElementById('modalTexto').innerText = conteudo;

